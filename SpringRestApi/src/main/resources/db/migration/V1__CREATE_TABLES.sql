CREATE TABLE usr (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    password varchar(64) NOT NULL,
    username varchar(64) NOT NULL UNIQUE ,
    PRIMARY KEY(id)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8 ;

CREATE TABLE todo (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    title varchar(64) NOT NULL,
    completed BOOLEAN NOT NULL,
    PRIMARY KEY(id)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8 ;